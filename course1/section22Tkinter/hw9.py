from tkinter import *
root = Tk()
def msg1():
    print('clicked New Project')
def msg2():
    print('clicked Undo')
def msg3():
    print('clicked Tool Windows')
def msg4():
    print('clicked Back')
def msg5():
    print('clicked Override Methods')
