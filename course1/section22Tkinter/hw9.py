root.config(menu=main_menu)
sub_menu1 = Menu(main_menu)
main_menu.add_cascade(label='File', menu=sub_menu1)
sub_menu1.add_command(label='New Project', command=msg1)
sub_menu1.add_command(label='New')
sub_menu1.add_separator()
sub_menu1.add_command(label='Open')
sub_menu1.add_command(label='Save As')
sub_menu1.add_separator()
sub_menu1.add_command(label='Open Recent')
sub_menu1.add_command(label='Close Projects in Current Window')
sub_menu1.add_separator()
sub_menu1.add_command(label='Exit', command=root.quit)


sub_menu2 = Menu(main_menu)
main_menu.add_cascade(label='Edit', menu=sub_menu2)
sub_menu2.add_command(label='Undo', command=msg2)
sub_menu2.add_command(label='Redo')
sub_menu2.add_separator()
sub_menu2.add_command(label='Cut')
sub_menu2.add_command(label='Copy')
sub_menu2.add_separator()
sub_menu2.add_command(label='Copy Path or Reference')
sub_menu2.add_command(label='Paste')

sub_menu3 = Menu(main_menu)
main_menu.add_cascade(label='View', menu=sub_menu3)
sub_menu3.add_command(label='Tool Windows', command=msg3)
sub_menu3.add_command(label='Appearance')
sub_menu3.add_separator()
sub_menu3.add_command(label='Quick Definition')
sub_menu3.add_command(label='Quick Type Definition')
sub_menu3.add_separator()
sub_menu3.add_command(label='Parameter Info')
sub_menu3.add_command(label='Type Info')

