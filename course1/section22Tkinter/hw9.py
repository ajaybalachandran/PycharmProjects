main_menu.add_cascade(label='Navigate', menu=sub_menu4)
sub_menu4.add_command(label='Back', command=msg4)
sub_menu4.add_command(label='Forward')
sub_menu4.add_separator()
sub_menu4.add_command(label='Search Everywhere')
sub_menu4.add_command(label='Class')
sub_menu4.add_separator()
sub_menu4.add_command(label='File')
sub_menu4.add_command(label='Symbol')

sub_menu5 = Menu(main_menu)
main_menu.add_cascade(label='Code', menu=sub_menu5)
